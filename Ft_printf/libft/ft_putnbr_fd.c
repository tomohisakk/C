#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	int		i;
	long	num;

	num = n;
	i = 1;
	if (n < 0)
	{
		num *= -1;
		write(fd, "-", 1);
	}
	while (n / 10 != 0)
	{
		n /= 10;
		i *= 10;
	}
	while (i > 0)
	{
		c = '0' + (num / i);
		write(fd, &c, 1);
		num -= (num / i) * i;
		i /= 10;
	}
}
